    private void carregarCombobox() {
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();

        ArrayList<String> nomesMedicos = funcionarioDAO.recuperarNomesMedicos();
        for (String nome : nomesMedicos) {
            cbxMedicos.addItem(nome);
        }

        ArrayList<String> nomesEnfermeiros = funcionarioDAO.recuperarNomesEnfermeiros();
        for (String nome : nomesEnfermeiros) {
            cbxEnfermeiros.addItem(nome);
        }
    }

    private void listarValores() {
        try {
            PlantaoDAO plantaoDAO = new PlantaoDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaPlantao.getModel();
            model.setNumRows(0);

            ArrayList<PlantaoDTO> lista = plantaoDAO.pesquisarPlantao();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getCodigoPlantaoEnfermeiro(),
                    lista.get(num).getCodigoEnfermeiro(),
                    lista.get(num).getHorarioEntrada(),
                    lista.get(num).getHorarioSaida(),
                    lista.get(num).getDataPlantao(),
                    lista.get(num).getCodigoMedico()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro JFPlantao" + e);
        }
    }

    private void carregarValores() {
        int setar = tabelaPlantao.getSelectedRow();

        txtcodplantao.setText(tabelaPlantao.getModel().getValueAt(setar, 0).toString());
        cbxEnfermeiros.setSelectedItem(tabelaPlantao.getModel().getValueAt(setar, 1).toString());
        txthoraentrada.setText(tabelaPlantao.getModel().getValueAt(setar, 2).toString());
        txthorasaida.setText(tabelaPlantao.getModel().getValueAt(setar, 3).toString());
        txtdataplant.setText(tabelaPlantao.getModel().getValueAt(setar, 4).toString());
        cbxMedicos.setSelectedItem(tabelaPlantao.getModel().getValueAt(setar, 5).toString());
    }

    private void limparCampos() {
        txtcodplantao.setText("");
        cbxEnfermeiros.setSelectedIndex(0);
        cbxMedicos.setSelectedIndex(0);
        txthoraentrada.setText("");
        txthorasaida.setText("");
        txtdataplant.setText("");
    }

    private void alterarPlantao() {
        int codigoPlantao;
        String horarioEntrada, horarioSaida, nomeEnfermeiro, nomeMedico;
        Date dataPlantao;

        codigoPlantao = Integer.parseInt(txtcodplantao.getText());
        nomeEnfermeiro = cbxEnfermeiros.getSelectedItem().toString();
        nomeMedico = cbxMedicos.getSelectedItem().toString();
        horarioEntrada = txthoraentrada.getText();
        horarioSaida = txthorasaida.getText();

        try {
            java.util.Date utilDate = new SimpleDateFormat("dd/MM/yyyy").parse(txtdataplant.getText());
            dataPlantao = new java.sql.Date(utilDate.getTime());
        } catch (java.text.ParseException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao converter a data de vacinação.");
            return;
        }

        int codigoEnf = obterCodigoFuncionarioPorNome(nomeEnfermeiro);
        int codigoMed = obterCodigoFuncionarioPorNome(nomeMedico);

        PlantaoDTO plantaoDTO = new PlantaoDTO();
        plantaoDTO.setCodigoPlantaoEnfermeiro(codigoPlantao);
        plantaoDTO.setCodigoEnfermeiro(codigoEnf);
        plantaoDTO.setHorarioEntrada(horarioEntrada);
        plantaoDTO.setHorarioSaida(horarioSaida);
        plantaoDTO.setDataPlantao(dataPlantao);
        plantaoDTO.setCodigoMedico(codigoMed);

        PlantaoDAO plantaoDAO = new PlantaoDAO();
        plantaoDAO.alterarPlantao(plantaoDTO);
    }

    private int obterCodigoFuncionarioPorNome(String nomeFuncionario) {
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        return funcionarioDAO.obterCodigoFuncionarioPorNome(nomeFuncionario);
    }

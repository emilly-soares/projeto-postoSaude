    private void btnCadastrarPlantaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarPlantaoActionPerformed

        String nomeEnfermeiro, nomeMedico, horarioEntrada, horarioSaida;
        Date dataPlantao;
        int codigoEnfermeiro, codigoMedico, codigoPlantao = 0;

        nomeEnfermeiro = cbxEnfermeiros.getSelectedItem().toString();
        nomeMedico = cbxMedicos.getSelectedItem().toString();
        horarioEntrada = txthoraentrada.getText();
        horarioSaida = txthorasaida.getText();

        try {
            java.util.Date utilDate = new SimpleDateFormat("dd/MM/yyyy").parse(txtdataplant.getText());
            dataPlantao = new java.sql.Date(utilDate.getTime());
        } catch (java.text.ParseException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao converter a data do plantão.");
            return;
        }

        codigoEnfermeiro = obterCodigoFuncionarioPorNome(nomeEnfermeiro);
        codigoMedico = obterCodigoFuncionarioPorNome(nomeMedico);

        PlantaoDTO plantaoDTO = new PlantaoDTO();
        plantaoDTO.setCodigoPlantaoEnfermeiro(codigoPlantao);
        plantaoDTO.setCodigoEnfermeiro(codigoEnfermeiro);
        plantaoDTO.setHorarioEntrada(horarioEntrada);
        plantaoDTO.setHorarioSaida(horarioSaida);
        plantaoDTO.setDataPlantao(dataPlantao);
        plantaoDTO.setCodigoMedico(codigoMedico);

        PlantaoDAO plantaoDAO = new PlantaoDAO();

        plantaoDAO.cadastrarPlantao(plantaoDTO);

        listarValores();
        limparCampos();

    }//GEN-LAST:event_btnCadastrarPlantaoActionPerformed


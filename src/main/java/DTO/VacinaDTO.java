package DTO;

public class VacinaDTO {

    private String nomeVacina;
    private String formaAdministracao;
    private String viaAdministracao;
    private String lugarAdministracao;
    private int codigoVacina;
    private String descricao;

    public String getNomeVacina() {
        return nomeVacina;
    }

    public void setNomeVacina(String nomeVacina) {
        this.nomeVacina = nomeVacina;
    }

    public String getFormaAdministracao() {
        return formaAdministracao;
    }

    public void setFormaAdministracao(String formaAdministracao) {
        this.formaAdministracao = formaAdministracao;
    }

    public String getViaAdministracao() {
        return viaAdministracao;
    }

    public void setViaAdministracao(String viaAdministracao) {
        this.viaAdministracao = viaAdministracao;
    }

    public String getLugarAdministracao() {
        return lugarAdministracao;
    }

    public void setLugarAdministracao(String lugarAdministracao) {
        this.lugarAdministracao = lugarAdministracao;
    }

    public int getCodigoVacina() {
        return codigoVacina;
    }

    public void setCodigoVacina(int codigoVacina) {
        this.codigoVacina = codigoVacina;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
}
